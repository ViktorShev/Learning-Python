# Profundizando Sistemas de Numeracion

# Decimal
a = 10 

# Binario
a = 0b1010

# Octal
a = 0o12

# Hexadecimal
a = 0xA

# Convertitr a un tipo entero, incluyendo la base

# Base decimal (default)
a = int('23', 10)

# Base binaria
a = int('10111', 2)

# Base octal
a = int('27', 8)

# Base hexadecimal
a = int('17', 16)

# Base 5 (poco comun)
a = int('344', 5)

print(f'a: {a}')